	public function is_valid_country_code($country_code = '') {
		return (in_array($country_code, $this->iso_countries));
	}

	public function get_country_code($country) {	
		if (isset($this->iso_countries[strtoupper($country)])) {
			return strtoupper($country);
		}
		
		$rev_countries = array_flip($this->iso_countries);
		if (function_exists('array_change_key_case')) {
			$rev_countries = array_change_key_case($rev_countries);
			$lcountry = strtolower($country);
		}
		else {
			$lcountry = $country;
		}
		
		if (isset($rev_countries[$lcountry])) {
			return $rev_countries[$lcountry];
		}
		
		return $country;
	}

	public function get_country_name($country) {
		
		if (isset($this->iso_countries[strtoupper($country)])) {
			return $this->iso_countries[strtoupper($country)];
		}
		
		return $country;
	}

	public function get_state_name($state, $country = 'US') {
		if (strlen($country) > 2) {
			$country = $this->get_country_code($country);
		}
		
		$states = array();
		
		if ($country == 'US' && isset($this->us_states[strtoupper($state)])) {
			return $this->us_states[strtoupper($state)];
		}
		
		if ($country == 'CA' && isset($this->ca_provinces[strtoupper($state)])) {
			return $this->ca_provinces[strtoupper($state)];
		}

		return $state;
	}

